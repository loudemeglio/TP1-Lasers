package modelo;

public class Coordenada {
    private int columna;
    private int fila;

    public Coordenada(int columna, int fila) {
        this.columna = columna;
        this.fila = fila;
    }

    public int getColumna() {
        return columna;
    }

    public int getFila() {
        return fila;
    }
}




